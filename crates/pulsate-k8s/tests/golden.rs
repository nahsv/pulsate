//! Golden reconcile tests — cluster-free.
//!
//! These build Gateway API objects in memory, translate them to Flow text, and
//! assert both the exact generated source (golden) and that installing it into a
//! real [`ConfigStore`] yields the expected typed config. No apiserver is
//! touched: the reconcile core ([`pulsate_k8s::install`] / [`to_flow`]) is pure.

use pulsate_config::ConfigStore;
use pulsate_k8s::crd::{
    Gateway, GatewayClass, GatewayClassSpec, GatewaySpec, HTTPBackendRef, HTTPPathMatch, HTTPRoute,
    HTTPRouteMatch, HTTPRouteRule, HTTPRouteSpec, Listener, ParentRef,
};
use pulsate_k8s::{install, to_flow, CONTROLLER_NAME};

// ---- builders -------------------------------------------------------------

fn class(name: &str, controller: &str) -> GatewayClass {
    GatewayClass::new(
        name,
        GatewayClassSpec {
            controller_name: controller.to_string(),
        },
    )
}

fn listener(name: &str, host: Option<&str>, port: u16, proto: &str) -> Listener {
    Listener {
        name: name.to_string(),
        hostname: host.map(str::to_string),
        port,
        protocol: proto.to_string(),
    }
}

fn gateway(ns: &str, name: &str, class_name: &str, listeners: Vec<Listener>) -> Gateway {
    let mut g = Gateway::new(
        name,
        GatewaySpec {
            gateway_class_name: class_name.to_string(),
            listeners,
        },
    );
    g.metadata.namespace = Some(ns.to_string());
    g
}

fn parent(name: &str) -> ParentRef {
    ParentRef {
        name: name.to_string(),
        namespace: None,
        section_name: None,
    }
}

fn prefix_match(path: &str) -> HTTPRouteMatch {
    HTTPRouteMatch {
        path: Some(HTTPPathMatch {
            match_type: "PathPrefix".to_string(),
            value: path.to_string(),
        }),
        method: None,
    }
}

fn backend(name: &str, port: u16) -> HTTPBackendRef {
    HTTPBackendRef {
        name: name.to_string(),
        namespace: None,
        port: Some(port),
        weight: None,
    }
}

fn route(
    ns: &str,
    name: &str,
    parents: Vec<ParentRef>,
    hostnames: Vec<&str>,
    rules: Vec<HTTPRouteRule>,
) -> HTTPRoute {
    let mut r = HTTPRoute::new(
        name,
        HTTPRouteSpec {
            parent_refs: parents,
            hostnames: hostnames.into_iter().map(str::to_string).collect(),
            rules,
        },
    );
    r.metadata.namespace = Some(ns.to_string());
    r
}

// ---- tests ----------------------------------------------------------------

#[test]
fn single_http_route_golden_text_and_config() {
    let classes = vec![class("pulsate", CONTROLLER_NAME)];
    let gateways = vec![gateway(
        "default",
        "web",
        "pulsate",
        vec![listener("http", Some("app.example.com"), 80, "HTTP")],
    )];
    let routes = vec![route(
        "default",
        "app",
        vec![parent("web")],
        vec!["app.example.com"],
        vec![HTTPRouteRule {
            matches: vec![prefix_match("/api")],
            backend_refs: vec![backend("api-svc", 8080)],
        }],
    )];

    let text = to_flow(&classes, &gateways, &routes, CONTROLLER_NAME);

    let expected = "\
# Generated by the pulsate-k8s Gateway API controller. Do not edit.

upstream up_default_app_0 {
  target http://api-svc.default.svc:8080
}

site app.example.com {
  tls off
  route /api/* ~> proxy(@up_default_app_0)
}
";
    assert_eq!(text, expected, "generated Flow text drifted");

    // And it installs cleanly into the live store.
    let store = ConfigStore::load("seed", "").expect("empty seed compiles");
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).expect("installs");

    let gen = store.current();
    assert_eq!(gen.config.upstreams.len(), 1);
    assert_eq!(gen.config.upstreams[0].name, "up_default_app_0");
    assert_eq!(
        gen.config.upstreams[0].targets[0].url,
        "http://api-svc.default.svc:8080"
    );
    assert_eq!(gen.config.sites.len(), 1);
    let site = &gen.config.sites[0];
    assert_eq!(site.hosts[0].pattern, "app.example.com");
    assert_eq!(site.tls, pulsate_config::TlsMode::Off);
    let handler = site.routes[0].handler.as_ref().unwrap();
    assert_eq!(handler.name(), "proxy");
}

#[test]
fn https_listener_exact_match_and_method_predicate() {
    let classes = vec![class("pulsate", CONTROLLER_NAME)];
    let gateways = vec![gateway(
        "prod",
        "edge",
        "pulsate",
        vec![listener("https", Some("api.example.com"), 443, "HTTPS")],
    )];
    let routes = vec![route(
        "prod",
        "svc",
        vec![parent("edge")],
        vec!["api.example.com"],
        vec![HTTPRouteRule {
            matches: vec![HTTPRouteMatch {
                path: Some(HTTPPathMatch {
                    match_type: "Exact".to_string(),
                    value: "/healthz".to_string(),
                }),
                method: Some("get".to_string()),
            }],
            backend_refs: vec![backend("health", 9000)],
        }],
    )];

    let text = to_flow(&classes, &gateways, &routes, CONTROLLER_NAME);
    assert!(
        text.contains("  tls auto\n"),
        "HTTPS should map to tls auto"
    );
    assert!(
        text.contains("route = /healthz [method=GET] ~> proxy(@up_prod_svc_0)"),
        "exact + method predicate not rendered:\n{text}"
    );

    let store = ConfigStore::load("seed", "").unwrap();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).expect("installs");
    let gen = store.current();
    let route = &gen.config.sites[0].routes[0];
    assert_eq!(route.kind, pulsate_config::MatchKind::Exact);
    assert_eq!(route.method.as_deref(), Some("GET"));
    assert_eq!(gen.config.sites[0].tls, pulsate_config::TlsMode::Auto);
}

#[test]
fn weighted_backends_become_weighted_targets() {
    let classes = vec![class("pulsate", CONTROLLER_NAME)];
    let gateways = vec![gateway(
        "default",
        "web",
        "pulsate",
        vec![listener("http", Some("split.example.com"), 80, "HTTP")],
    )];
    let routes = vec![route(
        "default",
        "canary",
        vec![parent("web")],
        vec!["split.example.com"],
        vec![HTTPRouteRule {
            matches: vec![prefix_match("/")],
            backend_refs: vec![
                HTTPBackendRef {
                    name: "v1".to_string(),
                    namespace: None,
                    port: Some(80),
                    weight: Some(9),
                },
                HTTPBackendRef {
                    name: "v2".to_string(),
                    namespace: None,
                    port: Some(80),
                    weight: Some(1),
                },
            ],
        }],
    )];

    let text = to_flow(&classes, &gateways, &routes, CONTROLLER_NAME);
    assert!(
        text.contains("target http://v1.default.svc:80 weight=9"),
        "{text}"
    );
    // weight=1 is the default and is omitted for cleanliness.
    assert!(text.contains("target http://v2.default.svc:80\n"), "{text}");

    let store = ConfigStore::load("seed", "").unwrap();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).expect("installs");
    let up = &store.current().config.upstreams[0];
    assert_eq!(up.targets.len(), 2);
    assert_eq!(up.targets[0].weight, 9);
    assert_eq!(up.targets[1].weight, 1);
}

#[test]
fn routes_sharing_a_host_aggregate_without_collision() {
    // Two HTTPRoutes attach to the same gateway/host. They must fold into ONE
    // site (a host collision would be a hard compile error).
    let classes = vec![class("pulsate", CONTROLLER_NAME)];
    let gateways = vec![gateway(
        "default",
        "web",
        "pulsate",
        vec![listener("http", Some("shared.example.com"), 80, "HTTP")],
    )];
    let routes = vec![
        route(
            "default",
            "a",
            vec![parent("web")],
            vec!["shared.example.com"],
            vec![HTTPRouteRule {
                matches: vec![prefix_match("/a")],
                backend_refs: vec![backend("svc-a", 80)],
            }],
        ),
        route(
            "default",
            "b",
            vec![parent("web")],
            vec!["shared.example.com"],
            vec![HTTPRouteRule {
                matches: vec![prefix_match("/b")],
                backend_refs: vec![backend("svc-b", 80)],
            }],
        ),
    ];

    let store = ConfigStore::load("seed", "").unwrap();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).expect("no host collision");
    let gen = store.current();
    assert_eq!(gen.config.sites.len(), 1, "must be one aggregated site");
    assert_eq!(gen.config.sites[0].routes.len(), 2);
    assert_eq!(gen.config.upstreams.len(), 2);
}

#[test]
fn unmanaged_class_is_ignored() {
    // A GatewayClass for some other controller must be skipped entirely.
    let classes = vec![class("other", "someone-else.io/controller")];
    let gateways = vec![gateway(
        "default",
        "web",
        "other",
        vec![listener("http", Some("nope.example.com"), 80, "HTTP")],
    )];
    let routes = vec![route(
        "default",
        "app",
        vec![parent("web")],
        vec!["nope.example.com"],
        vec![HTTPRouteRule {
            matches: vec![prefix_match("/")],
            backend_refs: vec![backend("svc", 80)],
        }],
    )];

    let store = ConfigStore::load("seed", "").unwrap();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).expect("installs empty");
    let gen = store.current();
    assert!(gen.config.sites.is_empty());
    assert!(gen.config.upstreams.is_empty());
}

