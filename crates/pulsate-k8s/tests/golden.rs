#[test]
fn translation_is_deterministic() {
    let classes = vec![class("pulsate", CONTROLLER_NAME)];
    let gateways = vec![gateway(
        "default",
        "web",
        "pulsate",
        vec![listener("http", Some("d.example.com"), 80, "HTTP")],
    )];
    let routes = vec![route(
        "default",
        "app",
        vec![parent("web")],
        vec!["d.example.com"],
        vec![HTTPRouteRule {
            matches: vec![prefix_match("/x"), prefix_match("/y")],
            backend_refs: vec![backend("svc", 80)],
        }],
    )];

    let a = to_flow(&classes, &gateways, &routes, CONTROLLER_NAME);
    let b = to_flow(&classes, &gateways, &routes, CONTROLLER_NAME);
    assert_eq!(a, b, "translation must be byte-stable");

    // Installing twice produces the same snapshot id (content-addressed).
    let store = ConfigStore::load("seed", "").unwrap();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).unwrap();
    let id1 = store.current().snapshot.id();
    install(&store, &classes, &gateways, &routes, CONTROLLER_NAME).unwrap();
    let id2 = store.current().snapshot.id();
    assert_eq!(id1, id2);
}
